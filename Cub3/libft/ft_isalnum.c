#include "libft.h"

int		ft_isalnum(int ch)
{
	if ((ch >= '0' && ch <= '9') || (ch >= 'A' && ch <= 'Z')
			|| (ch >= 'a' && ch <= 'z'))
		return (1);
	else
		return (0);
}
